const albumList = document.querySelectorAll("#albumList li");
const songsContainer = document.getElementById("songsContainer");
const albumTitle = document.getElementById("albumTitle");


albumList.forEach(item => {

item.addEventListener("click", () => {

const album = item.dataset.album;

albumTitle.textContent = albumTitles[album];

loadSongs(album);

});

});


function loadSongs(album){

songsContainer.innerHTML = "";

const songs = albums[album];

if(!songs){
songsContainer.innerHTML = "<p>No songs added yet.</p>";
return;
}

songs.forEach(song => {

const card = document.createElement("div");

card.classList.add("song-card");

card.innerHTML = `
<a href="${song.link}" target="_blank">
<img src="${song.image}">
</a>
<p class="song-title">${song.title}</p>
`;

songsContainer.appendChild(card);

});

}

const searchBar = document.querySelector(".bar"); 
let currentAlbum = ""; 

albumList.forEach(item=>{
    item.addEventListener("click", ()=>{
        currentAlbum = item.dataset.album; 
        albumTitle.textContent = albumTitles[currentAlbum]; 
        searchBar.value = ""; 
        loadSongs(currentAlbum); 
    }); 
}); 

searchBar.addEventListener("input", (e)=>{
    const searchTerm = e.target.value.toLowerCase();
    if(!currentAlbum) return; 
    const filteredSongs = albums[currentAlbum].filter(song=>{
        return song.title.toLowerCase().includes(searchTerm); 
    }); 

    displaySongs(filteredSongs); 
});

function loadSongs(albumKey){
    displaySongs(albums[albumKey]); 
}

function displaySongs(songsArray){
    songsContainer.innerHTML = ""; 
    if (!songsArray || songsArray.length === 0) {
        songsContainer.innerHTML = "<p>NO SONGS!</p>";
        return;
    }

    songsArray.forEach(song => {
        const card = document.createElement("div");
        card.classList.add("song-card");
        card.innerHTML = `
            <a href="${song.link}" target="_blank">
                <img src="${song.image}">
            </a>
            <p class="song-title">${song.title}</p>
        `;
        songsContainer.appendChild(card);
    });
}

const albumSearch = document.getElementById("bar2");
const albumItems = document.querySelectorAll("#albumList li");

albumSearch.addEventListener("input", (e) => {
    const searchTerm = e.target.value.toLowerCase();

    albumItems.forEach(li => {
        // Get the text inside the <li> (e.g., "Night Visions (2012)")
        const albumName = li.textContent.toLowerCase();

        if (albumName.includes(searchTerm)) {
            // Show the item if it matches
            li.style.display = "block";
        } else {
            // Hide the item if it doesn't match
            li.style.display = "none";
        }
    });
});